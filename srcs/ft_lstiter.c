#include "../includes/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*tmp;

	tmp = lst;
	while (tmp != NULL && f != NULL)
	{
		f(tmp);
		tmp = tmp->next;
	}
}
